package com.clydao.estudoJavaBasico;

public class ExplorandoVariaveis {
    public static void main(String[] args) {
        // Declare e inicialize variáveis de diferentes tipos
        int numero = 10;
        double valorDecimal = 5.99;
        char letra = 'A';
        boolean verdadeiro = true;

        // Imprima as variáveis no console
        System.out.println("Número inteiro: " + numero);
        System.out.println("Valor decimal: " + valorDecimal);
        System.out.println("Caractere: " + letra);
        System.out.println("Booleano: " + verdadeiro);
    }
}