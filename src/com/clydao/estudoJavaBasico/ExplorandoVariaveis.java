package com.clydao.estudoJavaBasico;

public class ExplorandoVariaveis {
    public static void main(String[] args) {
        // Declare e inicialize variáveis de diferentes tipos
        int numero = 10;
        double valorDecimal = 5.99;
        char letra = 'A';
        boolean verdadeiro = true;

        // Imprima as variáveis no console
        System.out.println("Número inteiro: " + numero);
        System.out.println("Valor decimal: " + valorDecimal);
        System.out.println("Caractere: " + letra);
        System.out.println("Booleano: " + verdadeiro);

        int myNumber; // Declaração de uma variável do tipo inteiro
        double myDouble; // Declaração de uma variável do tipo ponto flutuante
        boolean myBoolean; // Declaração de uma variável do tipo booleano
        char myChar; // Declaração de uma variável do tipo caractere
        String myString; // Declaração de uma variável do tipo String, que é uma classe

        myNumber = 10; // Atribuição de valor à variável do tipo inteiro
        myDouble = 3.14; // Atribuição de valor à variável do tipo ponto flutuante
        myBoolean = true; // Atribuição de valor à variável do tipo booleano
        myChar = 'A'; // Atribuição de valor à variável do tipo caractere
        myString = "Hello, World!"; // Atribuição de valor à variável do tipo String

        int anotherNumber = 100;
        double anotherDouble = 9.81;
        boolean isJavaFun = true;
        char anotherChar = 'B';
        String anotherString = "Learning Java";

        System.out.printf("print de uma declaração ja inicializada " + " - " + anotherString + " - ");

    }
}